int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int j;

	i = 0;
	if (length >= 2)
		j = f(tab[i], tab[i + 1]) % 1;
	while (i < length - 1)
	{
		if (f(tab[i], tab[i + 1]) % 1 != j)
			return (0);
		i++;
	}
	return (1);
}
