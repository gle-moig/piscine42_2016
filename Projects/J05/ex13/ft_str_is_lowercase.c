int	ft_str_is_lowercase(char *str)
{
	int renvoi;

	renvoi = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'a' && *str <= 'z'))
			renvoi = 0;
		str++;
	}
	return (renvoi);
}
