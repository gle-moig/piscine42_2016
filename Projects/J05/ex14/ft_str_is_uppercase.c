int	ft_str_is_uppercase(char *str)
{
	int renvoi;

	renvoi = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'A' && *str <= 'Z'))
			renvoi = 0;
		str++;
	}
	return (renvoi);
}
