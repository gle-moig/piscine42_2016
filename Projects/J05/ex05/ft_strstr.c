char	*ft_strstr(char *str, char *to_find)
{
	int i;

	i = 0;
	while (to_find[i] != '\0' && *str != '\0')
	{
		if (*str == to_find[i])
			i++;
		else
			i = 0;
		str++;
	}
	if (*str == '\0')
		return (NULL);
	else
		return (str - i);
}
