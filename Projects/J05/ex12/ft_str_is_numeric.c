int	ft_str_is_numeric(char *str)
{
	int renvoi;

	renvoi = 1;
	while (*str != '\0')
	{
		if (!(*str >= '0' && *str <= '9'))
			renvoi = 0;
		str++;
	}
	return (renvoi);
}
