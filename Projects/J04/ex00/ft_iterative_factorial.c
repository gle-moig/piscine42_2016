int	ft_iterative_factorial(int nb)
{
	int ret;

	ret = 0;
	if (0 < nb && nb < 13)
	{
		ret = nb;
	}
	if (nb == 0)
	{
		ret = 1;
	}
	if (nb >= 12)
	{
		while (nb-- > 1)
		{
			ret *= nb;
		}
	}
	return (ret);
}
